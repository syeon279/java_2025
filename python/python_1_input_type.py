## 🔁 3교시: `input()`, `print()` 함수 실습
### 🎯 학습 목표
# - 사용자 입력을 받아 출력하는 방법 익히기
# - `print()` 함수의 다양한 활용 (`sep`, `end`) 이해하기
# - ChatGPT를 활용해 입력/출력 표현 방식을 확장하기

### 📝 이론
# | 함수      | 설명                                        | 예시                                     |
# | --------- | ------------------------------------------- | ---------------------------------------- |
# | `input()` | 사용자로부터 문자열 형태의 입력을 받는 함수 | `name = input("이름을 입력하세요: ")`    |
# | `print()` | 출력할 값을 콘솔에 표시하는 함수            | `print("안녕하세요!")`                   |
# | `sep`     | 값들 사이의 구분자 지정                     | `print("A", "B", sep="-")` → `A-B`       |
# | `end`     | 출력 후 끝나는 문자를 설정 (`\n` 기본값)    | `print("Hello", end="🙂")` → 줄바꿈 없음 |

