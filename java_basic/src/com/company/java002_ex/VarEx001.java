package com.company.java002_ex;

public class VarEx001 {

	public static void main(String[] args) {
		int apple = 1000;
		System.out.println("사과 가격은 " + apple + "입니다.");
		System.out.printf("사과 가격은 %d입니다.\n", apple);
		apple = 2000;
		System.out.println("사과 가격은 " + apple + "입니다.");
		System.out.printf("사과 가격은 %d입니다.", apple);
	}

}
